#include<bits/stdc++.h> 
using namespace std;
// for loop is used to print a statement multiple times
// syntax:- for(intialiization;condition;;updation)
int  main(){
    int i;
    for(i=1;i<=5;i++){
        cout << "Striver" << endl;
    }
    cout << i << endl;
    int j;
    for(j=5;j>=1;j--){
        cout<< "Striver " << j << endl;
    }
    cout << j << endl;
   return 0;
}